# Crie um algoritimo que leia  um número e mostre o seu dobro, triplo e
# a raiz quadrada

numero = int(input('Digite um número'))
dobro = numero * 2
triplo = numero * 3
raiz_quadrada = numero ** 0.5

print(f'O dobro do {numero} é: {dobro}\n')
print(f'O triplo do {numero} é: {triplo}\n')
print(f'A raiz quadrada do {numero} é: {raiz_quadrada:.2f}') 
