# Escreva um programa que leia um valor em metros e o exiba convertido em centimetros
#  e milimetros.

# Programa conversão em metros quadrados

medida = float(input('Informe a media em metros quadrados: '))
centimetros = medida * 100
milimetros = medida * 1000

print(f' {medida} metros é igual a {centimetros} centimetros.\n')
print(f' {medida} metros é igual a {milimetros} milimetros')
